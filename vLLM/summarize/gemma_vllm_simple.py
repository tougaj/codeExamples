    # üöÄ Batch inference
    outputs = llm.generate(prompts, sampling_params)

    # Extraction —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤
    summaries = [output.outputs[0].text.strip() for output in outputs]

    return summaries


