def check_health() -> bool:
    """
    –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç—ñ —Å–µ—Ä–≤–µ—Ä–∞
    """
    try:
        response = requests.get(f"{SERVER_ADDRESS}/health")
        print(f"‚úÖ –°–µ—Ä–≤–µ—Ä –¥–æ—Å—Ç—É–ø–Ω–∏–π. –ü–∞—Ä–∞–º–µ—Ç—Ä–∏:")
        pprint(response.json())
        return True
    except Exception as e:
        print(f"‚ùå –°–µ—Ä–≤–µ—Ä –Ω–µ–¥–æ—Å—Ç—É–ø–Ω–∏–π: {e}")
        return False


def print_results(texts: list[str], results: requests.Response, route: str):
    if results.status_code == 200:
        raw_data = results.json()
        # –ü–æ—Ç—ñ–º –≤–∞–ª—ñ–¥—É—î–º–æ –π–æ–≥–æ —è–∫ BatchResponse
        data: BatchResponse = BatchResponse.model_validate(raw_data)
        for i, (result, text) in enumerate(zip(data.results, texts), 1):
            print(f"üìÑ –¢–µ–∫—Å—Ç #{i}: {text[:50]}")
            print(f"Tokens: input ‚Äî {result.input_tokens}, output ‚Äî {result.output_tokens}")
            print(f"‚ú® {route}: {result.text}")
            print(f"‚ú≥Ô∏è –ü—Ä–∏—á–∏–Ω–∞ –∑—É–ø–∏–Ω–∫–∏: {result.finish_reason}\n")
        print(f"Total input tokens: {data.total_input_tokens}, total output tokens: {data.total_output_tokens}")
        print(f"‚è±Ô∏è Processing time: {data.processing_time:.2f} —Å–µ–∫—É–Ω–¥ ({data.processing_time/data.total_texts:.3f} sec/text)\n")
    else:
        print(f"–ü–æ–º–∏–ª–∫–∞: {results.status_code}, {results.text}")


def process(texts: list[str], route: str):
    print(f"{route}\n{'-'*30}")
    start_time = time.time()
    data: TextRequest = TextRequest(
        texts=texts,
        sampling_params=SamplingParamsRequest(temperature=0.7, top_p=0.9, max_tokens=8192)
    )
    results = requests.post(f"{SERVER_ADDRESS}/{route}", json=data.model_dump())
    end_time = time.time()
    print_results(texts, results, route)
    print(f"‚è±Ô∏è –í—ñ–¥–ø–æ–≤—ñ–¥—å –∑–∞–π–Ω—è–ª–∞ {end_time - start_time:.4f} —Å–µ–∫—É–Ω–¥\n")


